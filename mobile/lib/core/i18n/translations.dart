  static void setLanguage(String lang) {
    if (lang == 'system') {
      _lang = effectiveLanguage('system');
      return;
    }
    _lang = supportedLanguages.contains(lang) ? lang : 'en';
  }

  static String get lang => _lang;

  /// Resolves a language preference into a concrete supported code.
  ///
  /// - If [preference] is a supported code, returns it.
  /// - If [preference] == 'system', reads the platform locale and returns
  ///   it if supported.
  /// - Otherwise returns 'en'.
  static String effectiveLanguage(String preference) {
    if (supportedLanguages.contains(preference)) return preference;
    if (preference == 'system') {
      final sys = PlatformDispatcher.instance.locale.languageCode;
      if (supportedLanguages.contains(sys)) return sys;
    }
    return 'en';
  }

